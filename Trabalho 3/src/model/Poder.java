package model;

/**
 *
 * @author joeda
 */
public class Poder {

    private int id;
    private String nome;
    private int dano;
    private String natureza;

    public Poder() {
    }

    public Poder(int id, String nome, int dano, String natureza) {
        this.id = id;
        this.nome = nome;
        this.dano = dano;
        this.natureza = natureza;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getDano() {
        return dano;
    }

    public void setDano(int dano) {
        this.dano = dano;
    }

    public String getNatureza() {
        return natureza;
    }

    public void setNatureza(String natureza) {
        this.natureza = natureza;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

}
