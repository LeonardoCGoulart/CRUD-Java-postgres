                    public void ExibeRegistroHeroi(Heroi heroi) {
                        jTextAreaResult.setText("id: "+String.valueOf(heroi.getId())
                        +"\nnome: "+(heroi.getNome())
                        +"\nvelocidade: "+(String.valueOf(heroi.getVelocidade()))
                        +"\nforca: "+(String.valueOf(heroi.getForca())));
                    }
                    
                    public void ExibeRegistroArma(ArmaHeroi arma) {
                        jTextAreaResult.setText("id: "+String.valueOf(arma.getId())
                        +"\nnome: "+(arma.getNome())
                        +"\ndano: "+(String.valueOf(arma.getDano()))
                        +"\nmunicao "+(String.valueOf(arma.getMunicao())));
                    }
                    
                    public void ExibeRegistroPoder(Poder poder) {
                        jTextAreaResult.setText("id: "+String.valueOf(poder.getId())
                        +"\nnome: "+(poder.getNome())
                        +"\ndano: "+(String.valueOf(poder.getDano()))
                        +"\nnatureza "+(String.valueOf(poder.getNatureza())));
                    }

