    private void jButtonBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscaActionPerformed
        String buscaOque = jTextFieldBusca.getText();
        String nome = jTextFieldNome.getText();
        

        
        if (buscaOque.equals("heroi") || buscaOque.equals("arma") || buscaOque.equals("poder")) {
            if(buscaOque.equals("heroi")){
                HeroiDAO dao = new HeroiDAO();
                dao.CriaConexao();
                try {
                   dao.BuscaHeroi(nome);
                    if(dao.getRsdados().first()){
                        ExibeRegistroHeroi(dao.getHeroi());
                    }
                    else{
                        System.out.println("ATENÇÃO:");
                        System.out.println("nao encontrado, verifique capslock ou cadastre");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, ex);
                }
             }   
            if(buscaOque.equals("poder")){
                PoderDAO dao = new PoderDAO();
                dao.CriaConexao();
                try {
                   dao.BuscaPoder(nome);
                    if(dao.getRsdados().first()){
                        ExibeRegistroPoder(dao.getPoder());
                    }
                    else{
                        System.out.println("ATENÇÃO:");
                        System.out.println("nao encontrado, verifique capslock ou cadastre");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, ex);
                }
             }  
            if(buscaOque.equals("arma")){
                ArmaHeroiDAO dao = new ArmaHeroiDAO();
                dao.CriaConexao();
                try {
                   dao.BuscaArma(nome);
                    if(dao.getRsdados().first()){
                        ExibeRegistroArma(dao.getArmaHeroi());
                    }
                    else{
                        System.out.println("ATENÇÃO:");
                        System.out.println("nao encontrado, verifique capslock ou cadastre");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, ex);
                }
             }
        }
        else{
            System.out.println("ERROR, digite na busca: heroi ou poder ou arma");
        }
            

         
    }//GEN-LAST:event_jButtonBuscaActionPerformed

